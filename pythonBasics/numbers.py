# Fundamental Data Types
#Int and float, complex
print(2 + 4)
print(type(2 + 4))
print(2 - 4)
print(type(2 - 4))
print(2 * 4)
print(2 / 4)
print(type(2 / 4))
print(type(20 + 1.1))
print(9.9 + 1.1)
print(type(9.9 + 1.1))

print(2**2)  # power
print(2 // 4)  # rounded
print(5 // 4)
print(5 % 4)

# math functions
print(round(3.1))
print(round(3.6))
print(abs(-20))

#To get binary value
print(bin(2))

